export const leaderboardService = {
  async getLeaderboard() {
    const leaderboard = mockLeaderboard
      .sort((a, b) => b.score - a.score)
      .map((player, index) => ({
        rank: index + 1,
        ...player,
        kd:
          player.deaths > 0
            ? Number((player.kills / player.deaths).toFixed(2))
            : player.kills,
      }));

    return apiResponse(leaderboard);
  },
};